#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	i;
	size_t	len;

	i = 0;
	len = ft_strlen(src);
	while (i <= len)
	{
		dst[i] = src[i];
		i++;
	}
	return (dst);
}
